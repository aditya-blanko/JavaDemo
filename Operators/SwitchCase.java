public class SwitchCase {
    public static void main(String[] args) {
        int a = 4;
        switch(a)
        {
            case 1 : System.out.println("ok");
            break;
            case 2 : System.out.println("good");
            break;
            case 3 : System.out.println("well done");
            break;
            default : System.out.println("all good");
            break;
        }

    }
}
